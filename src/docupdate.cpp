namespace soplex
{

/*      \Section{C functions}
    For compiler optimization, the following functions are implemented in C.
 */
void UpdateUpdateVector
(
   double* res, double x,
   int n, const int* idx, const double* val
)
{
   while (--n >= 0)
      res[idx[n]] += x * val[idx[n]];
}
} // namespace soplex

